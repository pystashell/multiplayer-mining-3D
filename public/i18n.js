export function randomNickname(language, random = Math.random) {
  const normalized = normalizeLanguage(language);
  const parts = NICKNAME_PARTS[normalized];
  const pick = (values) => {
    const sample = Number(random());
    const bounded = Number.isFinite(sample) ? Math.min(0.999999, Math.max(0, sample)) : 0;
    return values[Math.floor(bounded * values.length)];
  };
  const adjective = pick(parts.adjectives);
  const noun = pick(parts.nouns);
  return normalized === 'zh' ? `${adjective}的${noun}` : `${adjective} ${noun}`;
}

export function initialLanguage() {
  let saved = null;
  try { saved = localStorage.getItem('holo-sweeper.language'); } catch {}
  return SUPPORTED_LANGUAGES.includes(saved) ? saved : normalizeLanguage(navigator.language);
}

export function translate(language, key, params = {}) {
  const template = translations[language]?.[key] ?? translations.zh[key] ?? key;
  return Object.entries(params).reduce((value, [name, replacement]) => value.replaceAll(`{${name}}`, String(replacement)), template);
}
